let idade = 31;
console.log(typeof 31); // a funcionalidade "typeof" nos retorna o tipo do dado que esta sendo passado como parâmetro para ele
console.log(typeof idade);

let salario = 4578.32; // O JavaScript não faz diferenciação entre números inteiros e números de ponto flutuante, considerando tudo como do tipo 'Number'
console.log(typeof salario);

